public class ClassA {

    private double d;

    private float f;

    private long l;

    private int i;

    private short s;

    private byte b;

    private char c;

    private String str;

    public double getD() {
        return d;
    }

    public void setD(double d) {
        this.d = d;
    }

    public float getF() {
        return f;
    }

    public void setF(float f) {
        this.f = f;
    }

    public long getL() {
        return l;
    }

    public void setL(long l) {
        this.l = l;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public short getS() {
        return s;
    }

    public void setS(short s) {
        this.s = s;
    }

    public byte getB() {
        return b;
    }

    public void setB(byte b) {
        this.b = b;
    }

    public char getC() {
        return c;
    }

    public void setC(char c) {
        this.c = c;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    @Override
    public String toString() {
        return "ClassA{" +
                "d=" + d +
                ", f=" + f +
                ", l=" + l +
                ", i=" + i +
                ", s=" + s +
                ", b=" + b +
                ", c=" + c +
                ", str='" + str + '\'' +
                '}';
    }
}
